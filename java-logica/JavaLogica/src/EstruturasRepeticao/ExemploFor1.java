package EstruturasRepeticao;

public class ExemploFor1 {

	public static void main(String[] args) {
		int x;
		System.out.println("\t Tabuada do 9: ");
		for(x = 1; x <= 10; x++)
			System.out.println("9 x " + x + " = " + 9 * x);
		System.out.println(x);
			
	}

}

// Laços são estruturas que permitem repetir 1 ou mais linhas de códigos
// Há 3 tipos de laços na programação
// FOR		WHILE		DO WHILE
// Usamos o laço for quando há determinado uma quantidade de vezes que deverá executar aquela linha de código
// for (x = 1; x <= 10; x++)
//		inicio/  fim /  passo/
